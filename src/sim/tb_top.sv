module tb_top;

logic clk;
logic reset;

logic [3:0] rows;
logic [3:0] cols;

logic [6:0] seg;
logic [1:0] an;

//////////////////////////////////////////////////////
// DUT
//////////////////////////////////////////////////////

top DUT (

    .clk(clk),
    .reset(reset),

    .rows(rows),

    .cols(cols),

    .seg(seg),

    .an(an)
);

//////////////////////////////////////////////////////
// CLOCK
//////////////////////////////////////////////////////

always #5 clk = ~clk;

//////////////////////////////////////////////////////
// INITIAL
//////////////////////////////////////////////////////

initial begin

    $dumpfile("tb_top.vcd");
    $dumpvars(0, tb_top);

    clk = 0;
    reset = 1;

    rows = 4'b0000;

    #20;
    reset = 0;

end

//////////////////////////////////////////////////////
// KEYPAD SIMULATION
//////////////////////////////////////////////////////

initial begin

    #100;

    //////////////////////////////////////////////////
    // tecla normal
    //////////////////////////////////////////////////

    rows = 4'b0001;

    #100;

    rows = 4'b0000;

    //////////////////////////////////////////////////
    // tecla A
    //////////////////////////////////////////////////

    #300;

    rows = 4'b0010;

    #100;

    rows = 4'b0000;

    //////////////////////////////////////////////////
    // tecla B
    //////////////////////////////////////////////////

    #300;

    rows = 4'b0100;

    #100;

    rows = 4'b0000;

    //////////////////////////////////////////////////

    #20000;

    $finish;

end

endmodule